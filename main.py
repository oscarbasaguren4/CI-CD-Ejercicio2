# Definir una función que suma dos números
def sumar_numeros(num1, num2):
    suma = num1 + num2
    return suma

# Definir dos variables con números
numero1 = 10
numero2 = 5

# Llamar a la función y mostrar la suma usando print
resultado = sumar_numeros(numero1, numero2)
print(f"La suma de {numero1} y {numero2} es: {resultado}")
